package com.aaron.smartplaylists;

/**
 * These are the operators that can be used for rules
 */
public enum Operator {
    CONTAINS,
    DOES_NOT_CONTAIN,
    IS,
    IS_NOT,
    STARTS_WITH,
    ENDS_WITH,
    LESS_THAN,
    LESS_THAN_OR_EQUAL,
    GREATER_THAN,
    GREATER_THAN_OR_EQUAL,
    AFTER,
    BEFORE,
    IN_THE_LAST,
    NOT_IN_THE_LAST
}
